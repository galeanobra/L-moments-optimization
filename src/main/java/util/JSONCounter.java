package util;

public class JSONCounter {
    public int numberOfBits;
    public int numberOfIntegers;
    public int numberOfDoubles;

    public JSONCounter(int numberOfBits, int numberOfIntegers, int numberOfDoubles) {
        this.numberOfBits = numberOfBits;
        this.numberOfIntegers = numberOfIntegers;
        this.numberOfDoubles = numberOfDoubles;
    }
}
